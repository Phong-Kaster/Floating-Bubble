fun customizedTextStyle(
    fontSize: Int = 14,
    fontWeight: Int = 400,
    lineHeight: Int = (fontSize * 1.5f).toInt(),
    color: Color = TextColor3
): TextStyle = TextStyle(
    fontSize = fontSize.sp,
    fontWeight = FontWeight(fontWeight),
    lineHeight = lineHeight.sp,
    color = color,
)